    </div>
  </div>
<!-- 弹出框 -->
<van-popup v-model="show" position="bottom" :overlay="false">
  <h3 class="title_tip">提示</h3>
  <span class="txt_tip">每天手动或超时取消订单超过3次，将被禁止交易24小时</span>
  <p class="actBtn submit" @click="cancelOrder(1)">取消订单</p>
  <p class="actBtn cancel" @click="cancelOrder(0)">继续交易</p>
</van-popup>
</div>
</template>

<script>
import { post } from '@/assets/js/fetch'
import CountDown from 'common/time/CountDown'
export default {
  name: 'Footer',
  components: {
    CountDown
  },
  props: {
    okTxt: String,
    showfooter: Number,
    pay_type: Number,
    order_no: Number
  },
  data () {
    return {
      show: false,
      endCountdown: false
    }
  },
  methods: {
    callback () {
      this.endCountdown = true
      console.log(this.endCountdown)
      alert(1111)
    },
    // 取消订单
    cancelOrder (index) {
      if (index === 0) {
        this.show = !this.show
        return false
      }
      var data = {
        token: localStorage.getItem('randomcode'),
        order_no: this.order_no
      }
      let url = this.$api.order + '/api/order/cancelRechangeOrder'
      post(url, data)
        .then(res => {
          console.log(res)
        })
        .catch(e => {
          console.log(e)
          this.$toast('网络错误4')
        })
    },
    // 确认付款
    submit () {
      var data = {
        token: localStorage.getItem('randomcode'),
        order_no: this.order_no,
        pay_type: this.pay_type
      }
      let url = this.$api.order + '/api/order/cancelRechangeOrder'
      post(url, data)
        .then(res => {
          console.log(res)
          this.$()
        })
        .catch(e => {
          console.log(e)
          this.$toast('网络错误4')
        })
    },
    appeal () {
      this.$router.push({
        path: '/appeal',
        query: {
          order_no: this.order_no
        }
      })
    }
  },
  filters: {
    textW: function (value) {
      value = value.toString()
      if (value === '4' || value === '8') {
        value = '操作失误导致订单被取消？'
      } else if (value === '5') {
        value = '钱包积分未产生变化或者订单信息有误？'
      }
      return value
    }
  }
}
</script>

