<template>
  <!-- 底部-列表 -->
  <div class="orders">
    <div class="tabs">
      <ul>
        <li v-for="(item, index) in tabTitle"
            :key="index"
            class="tabs-li"
            @click="selectTab(item.title)"
            :class="selectTitle === item.title ? 'tabs-li-select' : ''">
          {{item.title}}
        </li>
      </ul>
    </div>

    <div class="lists">
      <ul>
        <li
            v-for="(item, index) in list"
            :key="index"
            @click="jumpDetail()">
          <div class="li-tab-box">
            <div class="li-tab-text">{{item.充值}}</div>
            <div class="li-tab-time">2019-03-18 18:28:22</div>
            <div
                class="li-tab-status"
                :class="{
                  'li-tab-orange': item.状态 === '待付款',
                  'li-tab-blue': item.状态 === '已提交',
                  'li-tab-red': item.状态 === '未到账'
                }"
            >{{item.状态}}</div>
          </div>

          <div class="li-money">
            <div class="left">金额</div>
            <div class="right">￥{{item.金额}}</div>
          </div>

          <div class="li-money">
            <div class="left">积分</div>
            <div class="right">{{item.积分}}</div>
          </div>
        </li>
      </ul>
    </div>
  </div>
</template>

<script>
export default {
  name: 'HomeList',
  props: {
    list: Array
  },
  data () {
    return {
      selectTitle: '未完成',
      tabTitle: [
        {
          title: '未完成'
        },
        {
          title: '已完成'
        },
        {
          title: '已取消'
        }
      ]
    }
  },
  methods: {
    selectTab (title) {
      switch (title) {
        case '未完成':
          this.selectTitle = '未完成'
          break
        case '已完成':
          this.selectTitle = '已完成'
          break
        case '已取消':
          this.selectTitle = '已取消'
          break
        default:
          this.selectTitle = '未完成'
          break
      }
    },
    jumpDetail () {
      this.$router.push({ name: 'Detail' })
    }
  }
}
</script>

